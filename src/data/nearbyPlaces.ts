export function calculateDistance(lat1: number, lon1: number, lat2: number, lon2: number): number {
  const R = 6371
  const dLat = (lat2 - lat1) * Math.PI / 180
  const dLon = (lon2 - lon1) * Math.PI / 180
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLon / 2) * Math.sin(dLon / 2)
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a))
  return R * c
}

export function getNearbyPlaces(
  userLat: number,
  userLng: number,
  maxDistanceKm: number = 5,
  categories?: PlaceCategory[]
): (NearbyPlace & { distance: number })[] {
  return nearbyPlaces
    .map(place => ({
      ...place,
      distance: calculateDistance(userLat, userLng, place.lat, place.lng),
    }))
    .filter(place => place.distance <= maxDistanceKm)
    .filter(place => !categories || categories.length === 0 || categories.includes(place.category))
    .sort((a, b) => a.distance - b.distance)
}
